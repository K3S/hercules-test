CREATE TABLE HERC.LIFTING (
    LIFT_ID INTEGER GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1, NO ORDER, NO CYCLE, NO MINVALUE, NO MAXVALUE, CACHE 20)  NOT NULL NOT HIDDEN ,
    LIFTER CHARACTER(25) NOT NULL NOT HIDDEN ,
    LIFT_WGT SMALLINT NOT NULL NOT HIDDEN ,
    LIFT_TIME CHARACTER(19) NOT NULL NOT HIDDEN ,
    PRIMARY KEY (LIFT_ID)
) NOT VOLATILE UNIT ANY KEEP IN MEMORY NO